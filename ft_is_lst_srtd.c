#include "libft.h"

int						ft_is_lst_srtd(t_list *lst)
{
	if (!lst)
		return (0);
	while (lst->next)
	{
		if (*((int*)(lst->content)) >= *((int*)(lst->next->content)))
			return (0);
		lst = lst->next;
	}
	return (1);
}
